package oops;

public class Person 
{
	int id;
	String name;
	Job jb;

	public int getId() 
	{
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Job getJb() {
		return jb;
	}

	public void setJb(Job jb) {
		this.jb = jb;
	}

}
